package com.ibm.ioes.newdesign.dto;

public class ChangeReasonMapping {

	private Long customerSegmentId;
	private Long changeTypeId;
	private Long subChangeTypeId;
	public Long getCustomerSegmentId() {
		return customerSegmentId;
	}
	public void setCustomerSegmentId(Long customerSegmentId) {
		this.customerSegmentId = customerSegmentId;
	}
	public Long getChangeTypeId() {
		return changeTypeId;
	}
	public void setChangeTypeId(Long changeTypeId) {
		this.changeTypeId = changeTypeId;
	}
	public Long getSubChangeTypeId() {
		return subChangeTypeId;
	}
	public void setSubChangeTypeId(Long subChangeTypeId) {
		this.subChangeTypeId = subChangeTypeId;
	}
	
	
	
}
